package com.example.note.entity;

public class NoteBook {
	/**
	 * 路径
	 */
	private String path;
	
	/**
	 * 名称
	 */
	private String name;

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
